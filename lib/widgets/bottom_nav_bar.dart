        BottomNavigationBarItem(
          icon: Icon(Icons.subscriptions),
          label: 'Подписки',
        ),
        BottomNavigationBarItem(
          icon: Icon(Icons.video_library),
          label: 'Библиотека',
        ),
      ],
    );
  }
}